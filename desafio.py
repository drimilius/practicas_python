# Obtener informaci√≥n personal
nombre1 = "Ana"
edad1 = 28
altura1 = 1.65

nombre2 = "Luis"
edad2 = 34
altura2 = 1.75

nombre3 = "Marta"
edad3 = 22
altura3 = 1.60

# Calcular el promedio de las edades
promedio_edades = (edad1 + edad2 + edad3) / 3

# Calcular el promedio de las alturas
promedio_alturas = (altura1 + altura2 + altura3) / 3

# Calcular el total de caracteres en los nombres
total_caracteres_nombres = len(nombre1) + len(nombre2) + len(nombre3)

# Imprimir los resultados
print(f"Promedio de edades: {promedio_edades:.2f}")
print(f"Promedio de alturas: {promedio_alturas:.2f} metros")
print(f"Total de caracteres en los nombres: {total_caracteres_nombres}")
